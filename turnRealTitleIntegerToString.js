export default function turnRealTitleIntegerToString(title) {
  switch (title) {
    case 0:
      return 'Üye'
    case 1:
      return 'Jade'
    case 2:
      return 'Pearl'
    case 3:
      return 'Emerald'
    case 4:
      return 'Sapphire'
    case 5:
      return 'Black Sapphire'
    case 6:
      return 'Red Sapphire'
    case 7:
      return 'Ruby'
    case 8:
      return 'Black Ruby'
    case 9:
      return 'Blue Ruby'
    case 10:
      return 'Diamond'
    case 11:
      return 'Black Diamond'
    case 12:
      return 'Red Diamond'
    case 13:
      return 'Blue Diamond'
    default:
      return 'Üye'
  }
}
